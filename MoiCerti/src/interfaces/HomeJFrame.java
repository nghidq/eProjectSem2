    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        while (tableModelReg.getRowCount() > 0) {
            tableModelReg.removeRow(0);
        }
        String[] colsReg = {"NAME", "CERTIFICATE", "EXAM DATE", "No"};
// đặt tiêu đề cột cho tableModel
        tableModelReg.setColumnIdentifiers(colsReg);

        TableSearch.setModel(tableModelReg);
        // Canh giữa tiêu đề trong bảng
        JTableHeader Thearder = TableSearch.getTableHeader();
        ((DefaultTableCellRenderer) Thearder.getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);
        try {
            showDataReg();
        } catch (SQLException ex) {
            Logger.getLogger(HomeJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSearchMouseClicked

