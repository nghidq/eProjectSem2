    private void showDataReg() throws SQLException {
        String IDCard = txtSearch.getText();
        ResultSet result = std.showIDCard(IDCard);
        try {
            while (result.next()) { // nếu còn đọc tiếp được một dòng dữ liệu
                String rows[] = new String[4];
                rows[0] = result.getString("STUDENT_NAME"); // lấy dữ liệu tại cột số 1 (ứng với mã so)
                rows[1] = result.getString("CERTIFICATE_NAME");
                rows[2] = result.getString("EXAM_DATE");
                rows[3] = result.getString("ID_NUM");
                tableModelReg.addRow(rows); // đưa dòng dữ liệu vào tableModel
            }
        } catch (SQLException e) {
        }
    }

