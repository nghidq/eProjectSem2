    private void showDataResult() throws SQLException {

        ResultSet result = regObj.showIDStuResult();
        try {
            while (result.next()) { // nếu còn đọc tiếp được một dòng dữ liệu
                String rows[] = new String[8];
                rows[0] = result.getString(1); // lấy dữ liệu tại cột số 1 (ứng với mã so)
                rows[1] = result.getString(2);
                rows[2] = result.getString(3);
                rows[3] = result.getString(4);
                rows[4] = result.getString(5);
                rows[5] = result.getString(6);
                rows[6] = result.getString(7);
                rows[7] = result.getString(8);
                tableModelResult.addRow(rows); // đưa dòng dữ liệu vào tableModel
            }
        } catch (Exception e) {
        }
    }

