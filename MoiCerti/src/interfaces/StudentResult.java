    private void clearData() throws SQLException {
//Lay chi so dong cuoi cung
        int n = tableModelResult.getRowCount() - 1;
        for (int i = n; i >= 0; i--) {
            tableModelResult.removeRow(i);//Remove tung dong
        }
    }

