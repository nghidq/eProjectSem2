    private void btnExamMarkSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExamMarkSaveMouseClicked
        // TODO add your handling code here:
                   //cbtIDNum,txtStuName,cbtIDSubject,txtSubName,txtMark
        //btnExamMarkAdd
        
        String idSub = (String) cbtIDSubject.getSelectedItem();
        String subName = txtSubName.getText();
        String idNum = (String) cbtIDNum.getSelectedItem();
        String stuName = txtStuName.getText();
        float mExam = Float.parseFloat(txtMark.getText());
        if (idSub.length() == 0) {
            JOptionPane.showMessageDialog(null, "Subject is empty. Please add subject at a from left!", "Message", 1);
            txtMark.requestFocus();
        } else if (idNum.length() == 0) {
            JOptionPane.showMessageDialog(null, "Identity number of student is empty. Please add student at a from left!", "Message", 1);
            txtMark.requestFocus();        
        } else if (mExam < 0) {
            JOptionPane.showMessageDialog(null, "Mark is less zero . Please add number of over than zero at a from left!", "Message", 1);
            txtMark.requestFocus();        
        }else {
            try {
                if (flag == true) {
                    examObj.insertDataExam(idSub, idNum, mExam);
                    jLabelNotify.setVisible(true);
                } else {
                    examObj.editDataExam(idSub, idNum, mExam);
                    jLabelNotify.setVisible(true);
                }

                clearDataExam(); //goi ham xoa du lieu tron tableModel
                showDataExam(); //Do lai du lieu vao Table Model
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Updated successfull",
                        "Message", 1);
            }
            setClockExam(false);
            setButtonExam(true);
        }
    }//GEN-LAST:event_btnExamMarkSaveMouseClicked

