    private void showDataExam() throws SQLException {
        ResultSet result = examObj.showExam();
        try {
            while (result.next()) { // nếu còn đọc tiếp được một dòng dữ liệu
                String rows[] = new String[5];
                rows[0] = result.getString(1); // lấy dữ liệu tại cột số 1 (ứng với mã so)
                rows[1] = result.getString(2);
                rows[2] = result.getString(3);
                rows[3] = result.getString(4);
                rows[4] = result.getString(5);
                tableModelExam.addRow(rows); // đưa dòng dữ liệu vào tableModel
            }
        } catch (SQLException e) {
        }
    }
//Ham xoa du lieu trong tableModel

    private void clearDataExam() throws SQLException {
//Lay chi so dong cuoi cung
        int n = tableModelExam.getRowCount() - 1;
        for (int i = n; i >= 0; i--) {
            tableModelExam.removeRow(i);//Remove tung dong
        }
    }

