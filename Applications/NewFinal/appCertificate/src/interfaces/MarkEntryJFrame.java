    private void btnExamMarkDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExamMarkDeleteMouseClicked
        // TODO add your handling code here:

                jLabelNotify.setVisible(false);
        String idIdNum = (String) cbtIDNum.getSelectedItem();
        String idSub = (String) cbtIDSubject.getSelectedItem();
        try {
            if (idIdNum.length() == 0 && idSub.length() == 0) {
                JOptionPane.showMessageDialog(null, "Choose a ID item for delete",
                        "Message", 1);
            } else {
                if (JOptionPane.showConfirmDialog(null, "Do you want to delete " + idSub + " and " + idIdNum + "this item ? ", "Message", 2) == 0) {
                    examObj.deleteDataExam(idSub,idIdNum);//goi ham xoa du lieu theo ma loai
                    clearDataExam();//Xoa du lieu trong tableModel
                    showDataExam();//Do du lieu vao table Model
                    setNullExam();//Xoa trang Textfield
                    jLabelNotify.setVisible(true);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Fail", "Message", 1);
        }
    }//GEN-LAST:event_btnExamMarkDeleteMouseClicked

